// string → used to store text values
let str: string = "Hello";
console.log("String value:", str); // Output: Hello

// number → used to store numbers (integer or decimal)
let num: number = 10;
console.log("Number value:", num); // Output: 10

// boolean → used for true/false values
let bool: boolean = true;
console.log("Boolean value:", bool); // Output: true

// null → intentionally empty value
let n: null = null;
console.log("Null value:", n); // Output: null

// undefined → variable declared but not assigned
let u: undefined = undefined;
console.log("Undefined value:", u); // Output: undefined

// symbol → always creates a unique value
let sym: symbol = Symbol("key");
console.log("Symbol value:", sym); // Output: Symbol(key)

// bigint → used for very large numbers
let big: bigint = 123456789n;
console.log("BigInt value:", big); // Output: 123456789n
